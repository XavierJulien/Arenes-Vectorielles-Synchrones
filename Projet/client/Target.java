public class Target{
  private float x;
  private float y;

  public Target(float x,float y){
    this.x = x;
    this.y = y;
  }

  //GETTERS
  public float getX(){return x;}
  public float getY(){return y;}
  //SETTERS
  public void setX(float x){this.x = x;}
  public void setY(float y){this.y = y;}
}
